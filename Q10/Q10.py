# Define complex numbers
complex1 = complex(3, 2)
complex2 = complex(1, 7)

# Addition
addition_result = complex1 + complex2

# Subtraction
subtraction_result = complex1 - complex2

# Multiplication
multiplication_result = complex1 * complex2

# Division
division_result = complex1 / complex2

print(f"Complex1 + Complex2 = {addition_result}")
print(f"Complex1 - Complex2 = {subtraction_result}")
print(f"Complex1 * Complex2 = {multiplication_result}")
print(f"Complex1 / Complex2 = {division_result}")
